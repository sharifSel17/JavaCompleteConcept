package com.coreConcept.switchCase;

public class Switch_Case_Break {

    public static void main(String[] args) {
        int num2 = 2;
        switch (num2){
            case 1:
                System.out.println("value is :");
                break;
            case 2:
                System.out.println("value is :");
                break;
            case 3:
                System.out.println("value is :");
                break;
            case 4:
                System.out.println("value is :");
                break;
            default:
                System.out.println("value is none :");

        }
    }


}
