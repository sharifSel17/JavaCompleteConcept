package com.coreConcept.loops;

public class While_Loop {
//
public static void main(String[] args) {
    int num =5;
    while (num>0){
        System.out.println("array list value :"+num);
        num--;
    }
    //iterating an array using while loop

    int arr[] = {2,3,4,5};
    int i = 0;
    while (i<3){
        System.out.println("value is :"+arr[i]);
        i++;
    }
}
}
